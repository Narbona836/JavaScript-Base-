const minhaIdae = 30;
const suaIdade = 25;

comparacao = minhaIdae == suaIdade;
console.log("Igualdade:", comparacao);

comparacaoIdentica = minhaIdae === suaIdade;
console.log("Identicade:", comparacaoIdentica);

comparacaoMaior = minhaIdae > suaIdade;
console.log("Maior que:", comparacaoMaior);

comparacaoMenor = minhaIdae < suaIdade;
console.log("Menor que:", comparacaoMenor);

comparacaoDiferente = minhaIdae != suaIdade;
console.log("Diferente:", comparacaoDiferente);

comparacaoMenorIgual = minhaIdae <= suaIdade;
console.log("Menor ou igual:", comparacaoMenorIgual);

comparacaoMaiorIgual = minhaIdae >= suaIdade;
console.log("Maior ou igual:", comparacaoMaiorIgual);